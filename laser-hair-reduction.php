                                <p>Laser hair removal is a relatively simple cosmetic procedure that focuses more on hair follicles. "During treatment, the laser emits light which is absorbed by the pigment in the hair," said Dr. Says Manish Ratnakar. The best dermatologist in Mumbai. </p>
<p>Laser Hair removal is a process of using light at the right wavelengths to target the color pigments in hair follicles (the root of the hair). The principle on which it works is known as selective photo-thermolysis, a fancy way of saying that the light selectively breaks cell structures of a specific color through generation of heat. In the case of laser hair removal, these are basal stem cells that lead to hair growth from the follicle. This helps de-activate the follicles from future growth. When we say selective, it also means that the laser does not affect other structures around it, which includes the skin. Laser hair reduction does not give desirable results in a single session. We must repeat the procedure multiple times to target hair follicles in different phases of growth. A total of 6-8 sessions are usually enough to permanently reduce 80-100% of hair growth. The principle is universal for all permanent hair removal machines used across the globe.</p>
