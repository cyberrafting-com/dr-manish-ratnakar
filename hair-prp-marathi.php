                                     <p><b>Step 1:</b> डॉक्टर टाळूचे मुख्य भाग ओळखतात ज्यावर उपचार करणे आवश्यक आहे. प्रभावित भागाच्या क्षेत्रानुसार प्लाझमाची मात्रा व्यवस्थित केली जाते.</p>
